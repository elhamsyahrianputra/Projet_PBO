package degdeganapp;

/**
 *
 * @author Asus
 */
public class Kelapa {

// Attribute
         
    // Berat Kelapa dalam Kilogram
    double Beratkelapa = 1.2;
    
    // Jumlah pesanan kelapa

    
    
    // Menghitung Total berat kelapa yang dibeli
    
    double totalBeratkelapa(int jumlah) {
        double total = jumlah * this.Beratkelapa;
        return total;
    }

}
